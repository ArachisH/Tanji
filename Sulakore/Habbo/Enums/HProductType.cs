namespace Sulakore.Habbo
{
    /// <summary>
    /// Specifies product types available in the catalog
    /// </summary>
    public enum HProductType
    {
        /// <summary>
        /// Represents a wall item.
        /// </summary>
        Item = 'i',
        /// <summary>
        /// Represents a floor item.
        /// </summary>
        Stuff = 's',
        /// <summary>
        /// Represents an effect.
        /// </summary>
        Effect = 'e',
        /// <summary>
        /// Represents a badge.
        /// </summary>
        Badge = 'b'
    }
}
