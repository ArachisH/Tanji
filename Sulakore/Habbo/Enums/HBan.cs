namespace Sulakore.Habbo
{
    /// <summary>
    /// Specifies the types of bans possible.
    /// </summary>
    public enum HBan
    {
        /// <summary>
        /// Represents a ban time of one hour.
        /// </summary>
        Hour = 0,
        /// <summary>
        /// Represents a ban time of one day.
        /// </summary>
        Day = 1,
        /// <summary>
        /// Represents a permanent ban time.
        /// </summary>
        Permanent = 2
    }
}