namespace Sulakore.Habbo
{
    /// <summary>
    /// Specifies dances a user can perform in the client.
    /// </summary>
    public enum HDance
    {
        /// <summary>
        /// Represents a non-dancing player.
        /// </summary>
        None = 0,
        /// <summary>
        /// Represents the default dance.
        /// </summary>
        Normal = 1,
        /// <summary>
        /// Represents the pogo mogo dance. (HC Only).
        /// </summary>
        PogoMogo = 2,
        /// <summary>
        /// Represents the duck funk dance. (HC Only)
        /// </summary>
        DuckFunk = 3,
        /// <summary>
        /// Represents the rollie dance. (HC Only)
        /// </summary>
        TheRollie = 4
    }
}