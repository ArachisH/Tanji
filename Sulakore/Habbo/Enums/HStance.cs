namespace Sulakore.Habbo
{
    /// <summary>
    /// Specifies stances that users can use in the client.
    /// </summary>
    public enum HStance
    {
        /// <summary>
        /// Represents a user that is standing.
        /// </summary>
        Stand = 0,
        /// <summary>
        /// Represents a user that is sitting.
        /// </summary>
        Sit = 1,
        /// <summary>
        /// Represents a user that is laying down.
        /// </summary>
        Lay = 2
    }
}