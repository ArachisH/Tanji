namespace Sulakore.Habbo
{
    /// <summary>
    /// Specifies the relationship status kinds.
    /// </summary>
    public enum HRelationship
    {
        /// <summary>
        /// Represents a neutral relationship.
        /// </summary>
        None = 0,
        /// <summary>
        /// Represents a relationship status which is visualized by heart.
        /// </summary>
        Heart = 1,
        /// <summary>
        /// Represents a relationship status which is visualized by smile.
        /// </summary>
        Smile = 2,
        /// <summary>
        /// Represents a relationship status which is visualized by skull.
        /// </summary>
        Bobba = 3
    }
}
