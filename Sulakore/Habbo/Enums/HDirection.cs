namespace Sulakore.Habbo
{
    /// <summary>
    /// Specifies compass locations of an object in the client.
    /// </summary>
    public enum HDirection
    {
        /// <summary>
        /// Represents an object facing north.
        /// </summary>
        North = 0,
        /// <summary>
        /// Represents an object facing north east.
        /// </summary>
        NorthEast = 1,
        /// <summary>
        /// Represents an object facing east.
        /// </summary>
        East = 2,
        /// <summary>
        /// Represents an object facing south east.
        /// </summary>
        SouthEast = 3,
        /// <summary>
        /// Represents an object facing south.
        /// </summary>
        South = 4,
        /// <summary>
        /// Represents an object facing south west.
        /// </summary>
        SouthWest = 5,
        /// <summary>
        /// Represents an object facing west.
        /// </summary>
        West = 6,
        /// <summary>
        /// Represents an object facing north west.
        /// </summary>
        NorthWest = 7
    }
}